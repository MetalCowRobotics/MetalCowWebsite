              <h1 id="daisy">Daisy<br>
                <small>Aerial Assist, <em>2014</em></small>
              </h1>
              <p>
                MetalCow’s third year saw even more success. We
                finished our robot early and designed a robot to
                compete in all phases of the game <i>Aerial Assist.</i>
                Fifteen members competed in the first week of
                competition at the Central Illinois Regional at the
                Pekin, IL AvantiDome and the 4th week of competition at
                the Purdue Boilermaker Regional. Along with our
                Creativity Award for our unique catching design, we
                earned alliance selections on the second seeded
                alliance in Pekin and the number one pick on the top
                seeded alliance at Purdue. More than anything, we had
                proven ourselves as an up and coming veteran team which
                helped secure a large corporate donation to expand our
                program.
              </p>
              <p>
                <img class="alignleft" src="" alt="20140215_114341.jpg" width="426" height="568">
              </p>
              <p>
                Aerial Assist is a game styled similarly to a over
                -sized basketball game. Robots must either catch or
                pick up 25in diameter exercise balls from the ground or
                from the human player. There are multiple ways to score
                points. A high goal is equal to 10 points and a low
                goal is equal to 1 point. If you pass once you gain an
                extra 10 point for a total of 20 when you score. If you
                pass twice you gain an extra 20 point for a total of 40
                when you score. You are also able to throw the ball
                over a 6 foot tall truss in the middle of the field for
                an immediate 10 points, also if a robot catches the
                ball thrown over the truss you gain another immediate
                10 point bonus.
              </p>
              <p>
                Daisy was designed for easy catching and throwing; it
                is able to do this because we extend our sides out for
                a total of 17 square foot catcher which feeds into our
                catapult that has an estimated 150 lbs launch force.
                Daisy has four meccanum wheels which, in theory, allow
                it to move in any direction facing forward except for
                up and down. It is also able to score into the high
                goal or the low goal.
              </p>
