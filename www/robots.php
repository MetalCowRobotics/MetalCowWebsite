              <h1 id="chippy">Chippy<br>
                <small>Ultimate Assist, <em>2013</em></small>
              </h1>
              <p>
                The team’s second year brought more members and a more
                complex robot. The team had 15 members. This required
                dividing the team members into sub-teams including
                mechanical/powerdrive, programming, marketing and
                logistics. The game was called <i>Ultimate Ascent</i>
                and involved throwing Frisbees at a target and climbing
                a pyramid. We participated at the Crossroads Regional
                in Terre Haute, IN. Although the team didn’t advance to
                the finals, they took great pride in successfully
                executing all the game elements. The team also
                participated in an animation contest with its
                production of <i>Jack and the Beanstalk</i> featuring
                our mascot MetalCow and was awarded the 2013 Award for
                Technical Excellence.
              </p>
              <p>
                <img class="alignright" src="" alt="Chippy.JPG">
              </p>
              <p>
                Ultimate Ascent is a game styled similarly to disc golf
                or ultimate frisbee. Robots must pick up or collect
                disks from feeder stations, and put them into wide
                goals 10′ off the ground. At the end of the game,
                robots can climb a metal pyramid to gain more points,
                with more being awarded for going higher.
              </p>
              <p>
                Chippy was designed with a dropout 2 -pneumatic, 2
                -omni wheel drive -train that enabled us to quickly
                service problems, as well as maneuver and drive quickly
                on the field. We use two pneumatic wheels that launch
                the disk out into the top goal. At the end of the game,
                we would latch two hooks on our shooter to the bottom
                bar to pull up off &nbsp;the ground for a 10 point
                bonus.
              </p>
              <hr>
              <h1 id="bessie">Bessie<br>
                <small>Rebound Rumble, <em>2012</em></small>
              </h1>
              <p>
                <img class="alignleft" src="" alt="Bessie.jpg">
              </p>
              <p>
                The team of 11 members spent 6 weeks in
                January/February 2012 building a robot to play in the
                2012 FIRST <i>Rebound Rumble</i> game. The robot was
                designed to shoot a basketball into the goal, play
                defense, and balance on a bridge. The first competition
                was at the Midwest Regional on March 2012 held at the
                UIC Pavilion in Chicago, IL. The goal going into the
                competition as a rookie team was to have a running
                robot. We arrived at the competition with two bags of
                partially completed assemblies and left with a fully
                functioning robot that earned us the Rookie Inspiration
                Award for our efforts and determination to continually
                build and modify at this competition.
              </p>
              <p>
                Rebound Rumble is a game styled similarly to
                basketball. Robots must collect basketballs and score
                them into goals of varying heights (More points for
                higher goals). Separating the two sides of the field
                are 3 teeter -totter “bridges”, and a bump. At the end
                of the game, 10 points are given for each robot
                balanced on a bridge.
              </p>
              <p>
                Bessie was designed to pick up and score balls into the
                low goal, and be able to push down the bridge so it
                could balance on and travel across.
              </p>


